package in.rays.associate;

public class Employee {

	private int empId;
	private  String name;
	private Address addd;
	
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Address getAddd() {
		return addd;
	}
	public void setAddd(Address addd) {
		this.addd = addd;
	}
}
