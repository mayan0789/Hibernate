package in.rays.associate;

public class Bid {

	private int id;
	private int amount;
	private int itenId;
	
	public Bid() {
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public int getItenId() {
		return itenId;
	}
	public void setItenId(int itenId) {
		this.itenId = itenId;
	}
}
